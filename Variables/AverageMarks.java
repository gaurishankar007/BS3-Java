
public class AverageMarks {
	public static void main(String[] args) {
		
		double math = 81.0;
		double english = 88.4;
		double science = 85.0;
		double health = 95.0;
		double sum = math + english + science + health;
		double average = sum/4;
		System.out.println("Your average marks is " + average + ".");
				
	}
}
